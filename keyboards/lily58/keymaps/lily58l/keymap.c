// Array of all bitmaps for convenience. (Total bytes used to store images in PROGMEM = 2112)
const int epd_bitmap_allArray_LEN = 4;
const char* epd_bitmap_allArray[4] = {
	epd_bitmap_new_0,
	epd_bitmap_new_1,
	epd_bitmap_new_2,
	epd_bitmap_new_3
};


void render_default_layer_state(void) {
    oled_write_ln("Layer", false);
    switch (get_highest_layer(layer_state|default_layer_state)) {
        case _QWERTY:
            oled_write_raw_P(epd_bitmap_allArray[0], sizeof(epd_bitmap_new_0));
            break;
        case _LOWER:
            oled_write_raw_P(epd_bitmap_allArray[1], sizeof(epd_bitmap_new_1));
            break;
        case _RAISE:
            oled_write_raw_P(epd_bitmap_allArray[2], sizeof(epd_bitmap_new_2));
            break;
        case _ADJUST:
            oled_write_raw_P(epd_bitmap_allArray[3], sizeof(epd_bitmap_new_3));
            break;
        default:
            oled_write_ln_P(PSTR("Undefined"), false);
    }
}

void render_status_main(void) {
    render_default_layer_state();
}

bool oled_task_user(void) {
  render_status_main();  // Renders the current keyboard state (layer, lock, caps, scroll, etc)
  return false;
}
